    private void btnSalvarGrupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarGrupoActionPerformed
        GrupoUsuarioBD bD = new GrupoUsuarioBD();
        if (edtNomeGrupo.getText().isEmpty() == false) {
                grupoUsuario = preencheGrupo();
                if (grupoUsuario.getId() > 0) {
                    try {
                    //atualiza
                    bD.updateGrupoUsuario(grupoUsuario);
                      JOptionPane.showMessageDialog(this, "Sucesso ao Atualizar Grupo");
                    } catch (Exception ex) {
                        Logger.getLogger(FormConfSistema.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(this, "Erro ao Atualizar Grupo");
                    }
                } else {
                    try {
                    //insere
                    bD.adicionarGrupoUsuario(grupoUsuario);
                    JOptionPane.showMessageDialog(this, "Sucesso ao inserir Grupo");
                    } catch (Exception ex) {
                        Logger.getLogger(FormConfSistema.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(this, "erro ao inserir Grupo");
                    }
                }
        } else {
            JOptionPane.showMessageDialog(this, "Nome do Grupo não pode estar vazio.");
        }
           preencheTableGrupoUsuarios();
        frameGruposUsuarios.dispose();
     
    }//GEN-LAST:event_btnSalvarGrupoActionPerformed

