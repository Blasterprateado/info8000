    private GrupoUsuario pegaSelecionado(){
     int linhaSelecionada = -1;
        GrupoUsuario grupo= null;
        linhaSelecionada = tableGrupoUsuarios.getSelectedRow();
        if (linhaSelecionada >= 0) { 
            int idGrupo = (int) tableGrupoUsuarios.getValueAt(linhaSelecionada, 0);
          
            
         try {
             grupo= new GrupoUsuarioBD().getGruposById(idGrupo);
         } catch (Exception ex) {
             Logger.getLogger(FormConfSistema.class.getName()).log(Level.SEVERE, null, ex);
         }
}else{
           JOptionPane.showMessageDialog(this, "É necessario selecionar uma linha.");
        }
        
        return grupo;
}
