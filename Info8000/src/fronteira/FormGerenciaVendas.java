    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         int linhaSelecionada = -1;
        Venda venda = null;
        linhaSelecionada = tableVenda.getSelectedRow();
        if (linhaSelecionada >= 0) {
          int idVenda = (int) tableVenda.getValueAt(linhaSelecionada, 0);

            venda = bd.getVendaById(idVenda);
            Cliente cliente = new ClienteBD().getClienteById(venda.getId_Cliente());
            edtNome.setText(cliente.getNome());
                   edtEndereco.setText(cliente.getEndereco());
                   edtBairro.setText(cliente.getBairro());
                   edtnumero.setText(String.valueOf(cliente.getNumero()));
                   edtCpf.setText(cliente.getCpf());
                   edtTel.setText(cliente.getTelefone());
                   frameDadosCliente.setLocationRelativeTo(null);
                   frameDadosCliente.setVisible(true);
                   
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma venda");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

