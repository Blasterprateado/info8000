    private void edtPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtPesquisaKeyPressed
        TabelaVendas tabela_model = (TabelaVendas) tableVenda.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela_model);
        tableVenda.setRowSorter(sorter);
        String text ="(?i)"+edtPesquisa.getText().toUpperCase();
        if (text.length() == 0)
        {
            sorter.setRowFilter(null);
        }
        else
        {
            try
            {
                if(rbCliente.isSelected()){
                    sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text, 1));
                }else if(rbFuncionario.isSelected()){
                    sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text,2));

                }else if(rbnNVenda.isSelected()){
                    sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text,0));

                }
            }
            catch (PatternSyntaxException pse)
            {
                System.err.println("Erro");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_edtPesquisaKeyPressed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        TabelaVendas tabela_model = (TabelaVendas) tableVenda.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela_model);
        tableVenda.setRowSorter(sorter);

        DateFormat f = DateFormat.getDateInstance(DateFormat.MEDIUM);
        String text ="(?i)"+f.format(SelecionaData.getDate());
        if (text.length() == 0)
        {
            sorter.setRowFilter(null);
        }
        else
        {
            try
            {

                sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text, 4));

            }
            catch (PatternSyntaxException pse)
            {
                System.err.println("Erro");
            }
        }

        System.out.println();        // TODO add your handling code here:
    }//GEN-LAST:event_btnPesquisarActionPerformed

