     private void preencheTabela(){
         try {
        listaVenda=bd.getListaVenda();
        model = new TabelaVendas(listaVenda);
        tableVenda.setModel(model);
        
        
        tableVenda.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {
                
                super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                //A coluna do status é 3
                Object ref = table.getValueAt(row, 5);//Coluna Status
                //Coloca cor em todas as linhas,COLUNA(5) que tem o valor "Cancelada"
                if (ref != null && ref.equals("Cancelada")) {//Se Status for igual a "Cancelada"
                    /* setBackground(Color.RED);//Preenche a linha de vermelho
                    setForeground(Color.WHITE);//E a fonte de branco*/
                    setBackground(new Color(Integer.valueOf(configuracao.getCorFundoCancelado())));
                    setForeground(new Color(Integer.valueOf(configuracao.getCorLetrasCancelado())));
                }else if(ref != null && ref.equals("Condicional")){
                   setBackground(new Color(Integer.valueOf(configuracao.getCorFundoCondicional())));
                    setForeground(new Color(Integer.valueOf(configuracao.getCorLetrasCondicional())));
                }
                    else {
                    boolean sel = isSelected;
                    if (sel == true) {
                            setBackground(getBackground());
                            setForeground(getForeground());
                    } else {//Se Status não for "Cencaleda" 
                            setBackground(Color.WHITE);//Preenche a linha de branco
                            setForeground(new Color(51, 51, 51));//E a fonte de preto
                    }
                }
                if(isSelected){
		                  setBackground(Color.blue);
		setForeground(getForeground()); 
	}
                
                return this;
            }
        });
        
        
        
        
        
    } catch (Exception ex) {
        Logger.getLogger(FormGerenciaVendas.class.getName()).log(Level.SEVERE, null, ex);
    }
     }
