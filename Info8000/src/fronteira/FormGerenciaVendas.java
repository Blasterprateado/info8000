    private void devolveEstoque(Venda venda){
        ProdutoBD produtoBd = new ProdutoBD();
        ItensVendaBD itensBd = new ItensVendaBD();
        Double qtdEstoqueProduto= 0.0;
        try {//devolver estoque Produtos
                    
                    List<ItensVenda> listaDevolverEstoque = itensBd.getListaItensVendaByIdVenda(venda.getId());
                    
                    
                    Iterator<ItensVenda> iterator = listaDevolverEstoque.iterator();
                    while(iterator.hasNext()){
                         ItensVenda itensVenda = iterator.next();
                         int qtd = itensVenda.getQtd();
                         Produto produto = produtoBd.getProdutoById(itensVenda.getId_produto());
                     qtdEstoqueProduto = produto.getQtd_estoque();
                qtdEstoqueProduto += qtd;
                produto.setQtd_estoque(qtdEstoqueProduto);
                    produtoBd.AtualizaProduto(produto);
                    }
                   
                } catch (Exception ex) {
                    Logger.getLogger(FormFinalizaVenda.class.getName()).log(Level.SEVERE, null, ex);
                      JOptionPane.showMessageDialog(this, "Erro ao devolver estoques produtos venda " + ex);
                }
                // devolver estoque produtos
                
                
                itensBd.removerItemByIdVenda(venda.getId());//remover itens da venda;
    }
    
    
    
    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
 int linhaSelecionada = -1;
        Venda venda = null;
        linhaSelecionada = tableVenda.getSelectedRow();
        if (linhaSelecionada >= 0) {
          int idVenda = (int) tableVenda.getValueAt(linhaSelecionada, 0);

            venda = bd.getVendaById(idVenda);
            venda.setSituacao(2);
            int resposta= JOptionPane.showConfirmDialog(this, "Confirma Cancelamento de Venda?","Atenção",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
          if(resposta==JOptionPane.YES_OPTION){  
     try {
         new VendaBD().AtualizaVenda(venda);
         new LancamentoCaixaBD().removerlancamentoCaixaByIdVenda(venda.getId());
         devolveEstoque(venda);
          JOptionPane.showMessageDialog(null, "Venda cancelada com sucesso");
          preencheTabela();
     } catch (Exception ex) {
         Logger.getLogger(FormGerenciaVendas.class.getName()).log(Level.SEVERE, null, ex);
         JOptionPane.showMessageDialog(null, "Erro ao cancelar Venda.");
     }
          
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma Venda");
        }
    }//GEN-LAST:event_btnCancelarActionPerformed
    }
