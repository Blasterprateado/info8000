    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
       TabelaOrdemServico tabela_model = (TabelaOrdemServico) tableOs.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela_model);
        tableOs.setRowSorter(sorter);
        
        DateFormat f = DateFormat.getDateInstance(DateFormat.MEDIUM);
        String text ="(?i)"+f.format(SelecionaData.getDate());
        if (text.length() == 0)
        {
             sorter.setRowFilter(null);
        }
        else
        {
             try
             {
                 
                   sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text, 6));
                 
             }
             catch (PatternSyntaxException pse)
             {
                   System.err.println("Erro");
             }
        } 
        
        
        
        
        
        System.out.println();        // TODO add your handling code here:
    }//GEN-LAST:event_btnPesquisarActionPerformed
    public void seleciona() {
        int linhaSelecionada = -1;
        OrdemServico ordem = null;
        linhaSelecionada = tableOs.getSelectedRow();
        if (linhaSelecionada >= 0) {
            idOs = (int) tableOs.getValueAt(linhaSelecionada, 0);

            ordem = bd.getOrdemSById(idOs);
            System.out.println(ordem);
            if (frame == null) {
                frame = new FormOrdemServico();
                frame.retornaOrdemServico(ordem);
                frame.setVisible(true);
            } else {
                frame.retornaOrdemServico(ordem);
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Cliente");
        }

    }
