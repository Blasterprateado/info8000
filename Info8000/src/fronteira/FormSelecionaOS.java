    private void edtPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtPesquisaKeyPressed
TabelaOrdemServico tabela_model = (TabelaOrdemServico) tableOs.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela_model);
        tableOs.setRowSorter(sorter);
        String text ="(?i)"+edtPesquisa.getText().toUpperCase();
        if (text.length() == 0)
        {
             sorter.setRowFilter(null);
        }
        else
        {
             try
             {
                 if(rbCliente.isSelected()){
                   sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text, 1));
                 }else if(rbFuncionario.isSelected()){
                 sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text,5));

                 }else if(rbNOs.isSelected()){
                sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text,0));

                 }
             }
             catch (PatternSyntaxException pse)
             {
                   System.err.println("Erro");
             }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_edtPesquisaKeyPressed

