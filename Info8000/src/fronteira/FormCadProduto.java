    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        
        int linhaSelecionada = -1;
        Produto produto = new Produto();
        linhaSelecionada = tabelaProduto.getSelectedRow();
        if (linhaSelecionada >= 0) { 
            
            idProdutos = (int) tabelaProduto.getValueAt(linhaSelecionada, 0);
            System.out.println(idProdutos);
            ProdutoBD dao = new ProdutoBD();
            System.out.println(idProdutos);
            
            produto= dao.getProdutoById(idProdutos);
            System.out.println(produto);
            this.produto=produto;
            btnIncluirStatus=1;
       
            PanelGuia.setSelectedIndex(1);
            edtCodigo.setText(String.valueOf(produto.getId()));
    edtCusto.setText(produto.getPreco_compra().toString());
    edtDescricao.setText(produto.getDescricao());
    edtEstoqueMinimo.setText(produto.getEstoque_minimo().toString());
    edtQuantidade.setText(produto.getQtd_estoque().toString());
    edtVenda.setText(produto.getPreco_venda().toString());
   
           
     ComboClasse.getModel().setSelectedItem(new ClasseProdutoBD().getClasseProdutoByDesc(produto.getClasse()));
            
    ComboFornecedor.getModel().setSelectedItem(new FornecedorBD().getFornecedorByNome(produto.getFornecedor()));
    ComboMarca.getModel().setSelectedItem(new MarcaBD().getMarcaByNome(produto.getMarca()));
    edtCodBarras.setText(String.valueOf(produto.getCodBarras()));
    
           
            
        } 
        else {
            JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
        }
         AbilidaOuDesabilitaBotoes();
         btnExcluir.setEnabled(false);
            btnAlterar.setEnabled(false);
            btnIncluir.setEnabled(false);
            PanelGuia.setEnabled(false);
        
        
    }//GEN-LAST:event_btnAlterarActionPerformed

