    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        Produto produto = preencheProduto();
        ProdutoBD bd = new ProdutoBD();
        if(edtCodigo.getText().isEmpty()){
            try {
                bd.adicionarProduto(produto);
                PanelGuia.setEnabled(true);
                PanelGuia.setSelectedIndex(0);
                AbilidaOuDesabilitaBotoes();
                
                JOptionPane.showMessageDialog(this,"Produto Gravado com sucesso", "Sucesso",JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                Logger.getLogger(FormCadProduto.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this,"Erro Ao Adicionar Produto " + ex);
            }
        }else{
            try {
                bd.AtualizaProduto(produto);
                PanelGuia.setEnabled(true);
                PanelGuia.setSelectedIndex(0);
                AbilidaOuDesabilitaBotoes();
                
                JOptionPane.showMessageDialog(this,"Produto Atualizado com sucesso", "Sucesso",JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                Logger.getLogger(FormCadProduto.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this,"Erro Ao Adicionar Produto " + ex);
            }
        }
        
        
        
    }//GEN-LAST:event_btnGravarActionPerformed

