    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if (edtDescricao.getText().isEmpty()==false && edtValor.getText().isEmpty()==false) {
            ContasPagar marca = preencheContas();
            ContasPagarBD bd = new ContasPagarBD();
            if (edtCodigo.getText().isEmpty()) {
                try {
                    bd.adicionarContasPagar(marca);
                    JOptionPane.showMessageDialog(this, "Sucesso ao adicionar " + marca.getDescricao().toString() + "");
                } catch (Exception ex) {
                    Logger.getLogger(FormContasPagar.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    marca.setId(Integer.valueOf(edtCodigo.getText()));
                    bd.AtualizaConta(marca);
                    JOptionPane.showMessageDialog(this, "Sucesso ao atualizar " + marca.getDescricao().toString() + "");
                } catch (Exception ex) {
                    Logger.getLogger(FormContasPagar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            PanelGuia.setSelectedIndex(0);
            btnIncluir.setEnabled(true);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            PanelGuia.setEnabled(true);
            preencheTabela();
            edtCodigo.setText("");
            edtDescricao.setText("");
            edtValor.setText("");
            comboSituacao.setSelectedIndex(-1);
            jDate.setDate(null);
        }else{
            JOptionPane.showMessageDialog(this, "Não é permitido campos nulos.");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

