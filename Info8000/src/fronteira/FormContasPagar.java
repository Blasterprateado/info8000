    private ContasPagar preencheContas() {
        ContasPagar contas = new ContasPagar();
        if (edtValor.getText().isEmpty()==false && edtDescricao.getText().isEmpty()==false && jDate.getDate()!=null) {
           
            contas.setValor(Double.valueOf(edtValor.getText()));
            contas.setDescricao(edtDescricao.getText());
            contas.setSituacao(comboSituacao.getSelectedIndex());
            
            java.sql.Date dataSql = new java.sql.Date(jDate.getDate().getTime());
            contas.setData(dataSql);
        }
        return contas;
    }

