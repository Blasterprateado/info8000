    private void relatorioCliente(String ordem, String filtro, Date dataini, Date datafim){
    
        InputStream inputStream = getClass().getResourceAsStream("/Relatorios/ReportVendaDatECliente.jasper");
     Map parametros = new HashMap();
        parametros.put("Cliente", filtro);
        parametros.put("DataIni", converterData(dataini));
         parametros.put("DataFim",converterData(datafim));
          parametros.put("ordem", ordem);
       
           
        try {
            ConectionSingleton conexaosingleton = ConectionSingleton.getInstancia();
         Connection connection = conexaosingleton.conectar();
 
            // abre o relatório
//            ReportUtils.openReport( "Relatorio de Vendas Por Data e Cliente", inputStream, parametros,
  //                  connection );
         FormProgressDialog pro = new FormProgressDialog( "Relatorio de Vendas Por Data e Cliente", inputStream, parametros,
    connection );
              pro.setVisible(true);
 
        } catch ( SQLException exc ) {
            exc.printStackTrace();
            //JOptionPane.showMessageDialog(this, "erro" +exc+edtObservacao.getText());
        } catch ( JRException exc ) {
            exc.printStackTrace();
            JOptionPane.showMessageDialog(this, "erro" +exc);
        } catch (Exception ex) {
//            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "erro" +ex);
        }
    
}
    
