    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
     String ordem=null ;
        // progressDialog();
     if(rbOrdemCliente.isSelected()){
         ordem = "cliente";
     }else if(rbOrdemVendedor.isSelected()){
         ordem = "funcionario";
     }else if(rbOrdemNumero.isSelected()){
         ordem = "id";
     }
        
      if(rbCliente.isSelected()){
          Cliente cliente = (Cliente) ComboParametro.getSelectedItem();
          if(dataFinal.getDate()!=null&&dataFinal.getDate()!=null){
          relatorioCliente(ordem, cliente.getNome(),dataInicial.getDate(),dataFinal.getDate());
          } else{
              JOptionPane.showMessageDialog(this, "Selecione uma data inicial e uma final");
          }
      }else if(rbVendedor.isSelected()){
          Funcionario funcionario =(Funcionario) ComboParametro.getSelectedItem();
          if(dataFinal.getDate()!=null&&dataFinal.getDate()!=null){
          relatorioFuncionario(ordem, funcionario.getNome(),dataInicial.getDate(),dataFinal.getDate());
          } else{
              JOptionPane.showMessageDialog(this, "Selecione uma data inicial e uma final");
          }
      }else if(rbNumeroVenda.isSelected()){
          int id=Integer.valueOf(edtNun.getText());
          relatorioNVenda(ordem, id);
      }else{
          if(dataFinal.getDate()!=null&&dataFinal.getDate()!=null){
          relatorioVendaData(ordem,dataInicial.getDate(),dataFinal.getDate());
             
          }else{
                JOptionPane.showMessageDialog(this, "Selecione uma data inicial e uma final"); 
          }
      }
     
    }//GEN-LAST:event_btnFiltrarActionPerformed

