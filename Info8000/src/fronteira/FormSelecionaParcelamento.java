public int verifica() {
        CaixaDiarioBD bd = new CaixaDiarioBD();
        CaixaBD caixabd = new CaixaBD();
        Caixa caixa = new Caixa();
        try {
            caixa = caixabd.getUltimoCaixaById(0);
            int ultimoidCaixa = caixa.getId();
            if (caixa.getStatus().equals("FECHADO")) {
                return 2;
            } else if (caixa.getStatus().equals("ABERTO")) {
                return 1;
            }
        } catch (Exception ex) {
            Logger.getLogger(FormCaixaDiario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erro Ao Obter Dados " + ex);
        }
        return 0;

    }
    
    public void lancaEmCaixa(Double valor, String documento) {
        int resposta = verifica();
        if (resposta == 1) {
            LancamentoCaixa lancamento = new LancamentoCaixa();
            LancamentoCaixaBD bd = new LancamentoCaixaBD();
            CaixaDiario caixadiario = new CaixaDiario();
            CaixaDiarioBD diariobd = new CaixaDiarioBD();

            lancamento.setTipo(documento);

            caixadiario.setTipolancamento(documento);
            lancamento.setObs("Venda");

            caixadiario.setValor(valor);
            lancamento.setValor(valor);

            try {
                bd.adicionarlancamentoCaixa(lancamento);
                diariobd.adicionarClasseCaixa(caixadiario);
                //JOptionPane.showMessageDialog(this,"Lançamento adicionado com sucesso");
            } catch (Exception ex) {
                Logger.getLogger(FormFinalizaVenda.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Erro ao realizar lancamento " + ex);
            }

            //this.dispose();

        } else {
            JOptionPane.showMessageDialog(this, "Caixa Fechado Abra-o");
        }

    }
    private void btnDarBaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarBaixaActionPerformed
int linhaSelecionada = -1;
        Parcelamento parcelamento = null;
        linhaSelecionada = TableParcelamento.getSelectedRow();
        if (linhaSelecionada >= 0) { 
           int  id = (int) TableParcelamento.getValueAt(linhaSelecionada, 0);
          ParcelamentoBD dao = new ParcelamentoBD();
          int resposta =JOptionPane.showConfirmDialog(this, "Confirma Recebimento?", "Confirma?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
          if(resposta==JOptionPane.YES_OPTION){
             try {
                 
                   parcelamento = dao.getItemById(id);
                   if(parcelamento.getStatus().equals("RECEBIDO")){
                     JOptionPane.showMessageDialog(this, "A baixa ja foi realizada, não é possivel realizar a ação novamente");
                 }else{
                    parcelamento.setStatus("RECEBIDO");
                    lancaEmCaixa(parcelamento.getValor(), parcelamento.getDocumento());
                   dao.AtualizaItem(parcelamento);
                   JOptionPane.showMessageDialog(this, "Recebimento Registrado com sucesso!");
                   }
               } catch (Exception ex) {
                   Logger.getLogger(FormSelecionaParcelamento.class.getName()).log(Level.SEVERE, null, ex);
                   JOptionPane.showMessageDialog(this, "Erro ao Alterar Situação " + ex);
               }
          }
        }  else{
            JOptionPane.showMessageDialog(this, "Selecione uma linha!");
        }
            // TODO add your handling code here:
    }//GEN-LAST:event_btnDarBaixaActionPerformed
