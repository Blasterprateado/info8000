    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(rbCliente.isSelected()){
           Cliente cliente = new ClienteBD().getClienteByCpf(edtCliente.getText());
           preencheTabela(bd.getItemByIdCliente(cliente.getId()));
    }else if(rbCodVenda.isSelected()){
            try {
                preencheTabela(bd.getItemByVenda(Integer.valueOf(edtCodVenda.getText())));
            } catch (NumberFormatException numberFormatException) {
                JOptionPane.showMessageDialog(this, "Insira Somente Numeros!!!");
            }
    }else if(rbData.isSelected()){
      
            preencheTabela(bd.getItemByData(edtSomenteData.getDate()));
    }else if(rbVencidas.isSelected()){
         preencheTabela(bd.getParcelamentosVencidos());
    }else if(rbIntervaloDatas.isSelected()){
         preencheTabela(bd.getParcelamentosByDatas(jDateDataInicial.getDate(), jDateDataFinal.getDate()));
    }
        
        if(chNotinha.isSelected()==true){
          SorterNotinha();
        }if(chSituacao.isSelected()==true){
            SorterSituação();
        }if(chNotinha.isSelected()==false && chSituacao.isSelected()==false){
            SorterLimpo();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

