private void SorterNotinha(){
    TabelaParcelamento tabela_model = (TabelaParcelamento) TableParcelamento.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela_model);
        TableParcelamento.setRowSorter(sorter);
        String text ="NOTINHA";
        if (text.length() == 0)
        {
             sorter.setRowFilter(null);
        }
        else
        {
             try
             {
                   sorter.setRowFilter(RowFilter.regexFilter("(?i)"+text,5));
             }
             catch (PatternSyntaxException pse)
             {
                   System.err.println("Erro");
             }
        }
}
