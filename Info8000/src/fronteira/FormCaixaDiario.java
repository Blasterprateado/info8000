    private void jButtonAbreCaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAbreCaixaActionPerformed
       if(caixa.getStatus().equals("FECHADO")){
          Double valorInicial= caixa.getValoratual();
          caixa= new Caixa();
           caixa.setStatus("ABERTO");
           caixa.setValorinicial(valorInicial);
           caixa.setValoratual(valorInicial);
           try {
               caixabd.adicionarClasseCaixa(caixa);
                jLabelStatus.setText("Caixa Aberto - Aguardando Lançamentos");
                JOptionPane.showMessageDialog(this,"CAIXA ABERTO COM SUCESSO!");
                jButtonAbreCaixa.setEnabled(false);
                jButtonExcluirLancamento.setEnabled(true);
            jButtonFazerLancamento.setEnabled(true);
            jButtonFecharCaixa.setEnabled(true);
            edtTotal.setText("R$ "+valorInicial);
           } catch (Exception ex) {
               Logger.getLogger(FormCaixaDiario.class.getName()).log(Level.SEVERE, null, ex);
               JOptionPane.showMessageDialog(this,"ERRO AO ABRIR CAIXA"+ ex);
           }
           
       }
       
    }//GEN-LAST:event_jButtonAbreCaixaActionPerformed

