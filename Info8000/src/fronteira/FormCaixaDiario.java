    private void jButtonExcluirLancamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirLancamentoActionPerformed
         int linhaSelecionada = -1;
       linhaSelecionada = jTableCaixa.getSelectedRow();
       if (linhaSelecionada >= 0) {
//           int idCaixa = (int) jTableCaixa.getValueAt(linhaSelecionada, 3);
          int idCaixaA = (int) model.getValor(linhaSelecionada, 4);
          int op=JOptionPane.showConfirmDialog(this, "Confirma exclusão?","Alerta",JOptionPane.YES_NO_OPTION);
          if(op==JOptionPane.YES_OPTION){
           bd.removerClasseCaixa(idCaixaA);
           model.removeCaixa(linhaSelecionada);
           jTableCaixa.setModel(model);
           JOptionPane.showMessageDialog(this,"Lançamento Excluido");
             try {
                 lista=bd.getListaClasseCaixa();
                 model= new TabelaCaixaDiario(lista);
            jTableCaixa.setModel(model);
            total=bd.getCaixaDiarioSoma();
            total=total+caixa.getValorinicial();
            edtTotal.setText("R$ "+total.toString());
             } catch (Exception ex) {
                 Logger.getLogger(FormCaixaDiario.class.getName()).log(Level.SEVERE, null, ex);
             }
          }
            
       } 
      else {
           JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
       }
    }//GEN-LAST:event_jButtonExcluirLancamentoActionPerformed

