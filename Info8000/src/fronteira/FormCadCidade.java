    private void edtBuscaCidadeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtBuscaCidadeKeyPressed
      if(evt.getKeyCode()==KeyEvent.VK_ENTER){
          if(edtBuscaCidade.getText().isEmpty()==false){
          listacidade.clear();
          try {
              listacidade.add(cidadeBD.getListaCidadeBYNOME(edtBuscaCidade.getText()));
          } catch (Exception ex) {
              Logger.getLogger(FormCadCidade.class.getName()).log(Level.SEVERE, null, ex);
          }
         
      }else{
              try {
                  listacidade = cidadeBD.getListaCidade();
              } catch (Exception ex) {
                  Logger.getLogger(FormCadCidade.class.getName()).log(Level.SEVERE, null, ex);
              }
          }
          
         preencheTabela();
      }
    }//GEN-LAST:event_edtBuscaCidadeKeyPressed

